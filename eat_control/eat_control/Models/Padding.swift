import UIKit

public struct Padding {
    
    var top: CGFloat
    var bottom: CGFloat
    var left: CGFloat
    var right: CGFloat
    
    public init(top: CGFloat, bottom: CGFloat, left: CGFloat, right: CGFloat) {
        self.top = top
        self.bottom = bottom
        self.left = left
        self.right = right
    }
    
}
